<h1>Switch Statement</h1>


<?php
$grade = "A";
switch($grade) {
case A:
echo "You are excellent!";
break;
case B:
echo "You are wonderful!";
break;
case C:
echo "You are good!";
break;
case D:
echo "You are good but could do more!";
break;
case E:
echo "That is not good enough!";
break;
default:
echo "Check your answer.";
}
?>
